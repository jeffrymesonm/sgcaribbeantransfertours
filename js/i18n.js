const SUPPORTED_LANGS = ['en', 'es', 'fr'];
const LANG_STORAGE_KEY = 'sariel-lang';
// sessionStorage (not localStorage): the site must always greet a NEW visit
// in English, but keep the visitor's choice while they navigate between
// pages within the same browser session — same pattern as the service-picker
// popup's "asked once per session" flag in js/service-picker.js.
const storedLang = sessionStorage.getItem(LANG_STORAGE_KEY);
let currentLang = SUPPORTED_LANGS.includes(storedLang) ? storedLang : 'en';

/**
 * Looks up a translation key in the active language (falling back to
 * English, then the key itself), interpolating any {token} placeholders.
 * @param {string} key    Dictionary key, e.g. "calc.reserve".
 * @param {Object} [vars] Map of {token: value} substitutions.
 * @returns {string} Translated, interpolated string.
 */
function t(key, vars) {
  let str = I18N[currentLang][key] ?? I18N.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) str = str.replaceAll(`{${k}}`, v);
  }
  return str;
}

/** @returns {string} Active language code, "en", "es" or "fr". */
function getLang() {
  return currentLang;
}

/** Re-renders every static [data-i18n] / [data-i18n-attr] element and document meta. */
function applyStaticTranslations() {
  document.documentElement.lang = currentLang;
  const page = document.body.dataset.page || 'home';
  const titleKey = `meta.${page}.title`;
  const descKey = `meta.${page}.description`;
  document.title = t(titleKey);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.innerHTML = t(el.dataset.i18n);
  });

  // Format: data-i18n-attr="attr1:key1|attr2:key2" — lets one element
  // translate several attributes (e.g. aria-label + placeholder) at once.
  document.querySelectorAll('[data-i18n-attr]').forEach((el) => {
    el.dataset.i18nAttr.split('|').forEach((pair) => {
      const [attr, key] = pair.split(':');
      el.setAttribute(attr, t(key));
    });
  });

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.setAttribute('content', t(descKey));
  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle) ogTitle.setAttribute('content', t(titleKey));
  const ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc) ogDesc.setAttribute('content', t(descKey));

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    const active = btn.dataset.lang === currentLang;
    btn.classList.toggle('is-active', active);
    btn.setAttribute('aria-pressed', String(active));
  });
}

/**
 * Switches the active language: persists it for the rest of this browser
 * session, re-renders static text and notifies listeners (the feature
 * modules) so JS-generated content re-renders too.
 * @param {string} lang "en", "es" or "fr".
 * @returns {void}
 */
function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang) || lang === currentLang) return;
  currentLang = lang;
  sessionStorage.setItem(LANG_STORAGE_KEY, lang);
  applyStaticTranslations();
  document.dispatchEvent(new CustomEvent('sariel:langchange', { detail: { lang } }));
}

document.querySelectorAll('.lang-btn').forEach((btn) => {
  btn.addEventListener('click', () => setLang(btn.dataset.lang));
});

applyStaticTranslations();
